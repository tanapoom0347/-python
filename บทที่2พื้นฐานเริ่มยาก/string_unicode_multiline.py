def demo_unicode():
    s = "I love Thailand."
    print(s)
    t = "I \u2764  Thailand." # unicode 16-vit \u0000
    print(t)

    # unicode 32-bit Enoji \U00000000
    z = "I \u2764  Thailand. \U0001f600 \U0001f920"
    print(z)

